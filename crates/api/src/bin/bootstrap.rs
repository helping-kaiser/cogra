//! Instance bootstrap — the one-shot setup step that brings a CoGra instance
//! into existence (network.md §2, architecture.md "Genesis bootstrap").
//!
//! It is **not** a request path and has no "first user to register" detection:
//! it runs once, by hand, when an instance is created. It:
//!
//! 1. ensures both stores' schemas are applied;
//! 2. generates the instance JWT signing key (if absent) and appends it to
//!    `.env`;
//! 3. writes the four genesis nodes + the genesis credential/profile rows +
//!    the first invite link, across one dual-store transaction;
//! 4. prints the first invite link — the capability the first real user
//!    registers through.
//!
//! Re-running is safe: a fully-bootstrapped instance re-surfaces its invite
//! link and writes nothing; a half-failed instance (graph committed, Postgres
//! empty) completes its Postgres half. The both-stores gate and its branches
//! live in [`api::bootstrap`].

use std::fs::OpenOptions;
use std::io::Write as _;

use anyhow::Context;
use api::auth::keys::generate_signing_key;
use api::auth::password::hash_password;
use api::bootstrap::{BootstrapOutcome, GenesisContent, run};
use sha2::{Digest, Sha256};

/// The canonical bot-defense hashtag seeded at network birth (network.md §2).
const BOT_DEFENSE_HASHTAG: &str = "bot-defense";
/// The genesis invite link's lifetime — long-lived and multi-use, so the
/// instance owner can onboard the first cohort.
const GENESIS_INVITE_TTL_DAYS: i64 = 365;

fn env_or(key: &str, default: &str) -> String {
    std::env::var(key).unwrap_or_else(|_| default.to_string())
}

/// SHA-256 hex digest of the canonical version-1 platform-guidelines document,
/// seeded onto `Network.guidelines_hash` (graph-data-model.md §:Network).
fn guidelines_hash() -> String {
    let path = concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/../../docs/instances/platform-guidelines.md"
    );
    match std::fs::read(path) {
        Ok(bytes) => format!("{:x}", Sha256::digest(&bytes)),
        Err(e) => {
            tracing::warn!(error = %e, "platform-guidelines.md unreadable; seeding a zero digest");
            format!("{:x}", Sha256::digest(b""))
        }
    }
}

const ENV_PATH: &str = ".env";

/// True when `.env` already carries a non-empty `JWT_SIGNING_KEY=` line. The
/// env-var check alone misses the key when `.env` exists but wasn't exported
/// into this shell — without this a re-run would append a second key.
fn env_file_has_signing_key() -> bool {
    match std::fs::read_to_string(ENV_PATH) {
        Ok(contents) => contents.lines().any(|line| {
            line.strip_prefix("JWT_SIGNING_KEY=")
                .is_some_and(|v| !v.trim().is_empty())
        }),
        Err(_) => false,
    }
}

/// Ensures `JWT_SIGNING_KEY` exists: leaves it untouched if already set in the
/// environment or present in `.env`, otherwise generates one and appends it to
/// `.env` (locked to `0o600`) so subsequent `make api` runs pick it up.
fn ensure_signing_key() -> anyhow::Result<()> {
    let already_set = std::env::var("JWT_SIGNING_KEY")
        .map(|v| !v.trim().is_empty())
        .unwrap_or(false);
    if already_set {
        tracing::info!("JWT_SIGNING_KEY already set — leaving it untouched");
        return Ok(());
    }
    if env_file_has_signing_key() {
        tracing::info!("JWT_SIGNING_KEY present in .env — leaving it untouched");
        return Ok(());
    }
    let key = generate_signing_key().context("generating JWT signing key")?;
    let mut env_file = OpenOptions::new()
        .create(true)
        .append(true)
        .open(ENV_PATH)
        .context("opening .env to write JWT_SIGNING_KEY")?;
    writeln!(
        env_file,
        "\n# Generated by `make bootstrap` — the instance JWT signing key (auth.md).\nJWT_SIGNING_KEY={key}"
    )
    .context("appending JWT_SIGNING_KEY to .env")?;
    // The signing key is the instance's root JWT secret; keep .env owner-only.
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        std::fs::set_permissions(ENV_PATH, std::fs::Permissions::from_mode(0o600))
            .context("restricting .env to mode 0o600")?;
    }
    tracing::info!("generated JWT_SIGNING_KEY and appended it to .env");
    Ok(())
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(tracing_subscriber::EnvFilter::from_default_env())
        .init();

    let database_url =
        std::env::var("DATABASE_URL").context("DATABASE_URL must be set (see .env.example)")?;
    let pool = postgres_store::connect(&database_url)
        .await
        .context("connecting to PostgreSQL")?;
    postgres_store::run_migrations(&pool)
        .await
        .context("running Postgres migrations")?;

    let memgraph_host = env_or("MEMGRAPH_HOST", "localhost");
    let memgraph_port = env_or("MEMGRAPH_PORT", "7687")
        .parse()
        .context("MEMGRAPH_PORT must be a port number")?;
    let graph = graph_engine::connect(&memgraph_host, memgraph_port)
        .await
        .context("connecting to Memgraph")?;
    graph_engine::schema::apply_schema(&graph)
        .await
        .context("applying graph constraints + indexes")?;

    // Idempotent — leaves an existing key untouched, so a re-run of a live
    // instance never appends a second one (the genesis writes don't need it).
    ensure_signing_key()?;

    let content = GenesisContent {
        username: env_or("GENESIS_USERNAME", "genesis"),
        email: env_or("GENESIS_EMAIL", "genesis@cogra.local"),
        hashtag_name: BOT_DEFENSE_HASHTAG.to_string(),
        guidelines_hash: guidelines_hash(),
        invite_ttl_days: GENESIS_INVITE_TTL_DAYS,
    };

    // Lazy: only the fresh and repair paths need the password, so a re-run of a
    // healthy instance never demands it.
    let password_hash = || -> anyhow::Result<String> {
        let password = std::env::var("GENESIS_PASSWORD").context(
            "GENESIS_PASSWORD must be set to bootstrap the genesis User (see .env.example)",
        )?;
        if password == "change-me-genesis" {
            tracing::warn!(
                "genesis password is the .env.example default — change it before any real use"
            );
        }
        hash_password(&password).map_err(|e| anyhow::anyhow!("genesis password rejected: {e}"))
    };

    match run(&pool, &graph, &content, password_hash).await? {
        BootstrapOutcome::Fresh {
            network_id,
            user_id,
            username,
            invite_link,
        } => {
            println!("Bootstrap complete.");
            println!("  Network singleton : {network_id}");
            println!("  Genesis User      : {user_id} (@{username}, moderator)");
            println!("  First invite link : {invite_link}");
            println!(
                "Register the first real user with this invite link via the `register` mutation."
            );
        }
        BootstrapOutcome::Repaired {
            user_id,
            username,
            invite_link,
        } => {
            println!("Bootstrap repaired the Postgres half of a partially-failed run.");
            println!("  Genesis User      : {user_id} (@{username}, moderator)");
            println!("  First invite link : {invite_link}");
        }
        BootstrapOutcome::AlreadyComplete {
            user_id,
            invite_link,
        } => {
            println!("Instance already bootstrapped — nothing to do.");
            println!("  Genesis User      : {user_id}");
            println!("  First invite link : {invite_link}");
        }
    }
    Ok(())
}
